int		ft_count_if(char **tab, int (*f)(char *))
{
	int cpt;

	if (!tab)
		return (0);

	cpt = 0;
	while (*tab)
	{
		if ((*f)(*tab++))
			cpt++;
	}
	return (cpt);
}
